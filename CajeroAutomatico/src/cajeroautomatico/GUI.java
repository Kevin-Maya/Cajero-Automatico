    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        // TODO add your handling code here:
        if(txtcantidad.getText().equals("")){
            opcdepositar=false;
            opcretirar=false;
            
            panelopcion.setVisible(false);
            
            btndepositar.setEnabled(true);
            btnretirar.setEnabled(true);
            
            txtcantidad.setText("");
            
            btn1.setEnabled(false);
            btn2.setEnabled(false);
            btn3.setEnabled(false);
            btn4.setEnabled(false);
            btn5.setEnabled(false);
            btn6.setEnabled(false);
            btn7.setEnabled(false);
            btn8.setEnabled(false);
            btn9.setEnabled(false);
            btn0.setEnabled(false);
            
            btnaceptar.setEnabled(false);
            btncancelar.setEnabled(false);
            btnborrar.setEnabled(false);          
        }
        else
        {
            if(opcdepositar == true){
                opcdepositar = false;
                nuevaCantidad =Double.parseDouble(String.valueOf(txtcantidad.getText()));
                atm.depositar(nuevaCantidad);
                
                txtsaldo.setText(String.valueOf(atm.obtenerSaldo()));
                panelopcion.setVisible(false);
                txtcantidad.setText("");
                btndepositar.setEnabled(true);
                btnretirar.setEnabled(true);
                
            btn1.setEnabled(false);
            btn2.setEnabled(false);
            btn3.setEnabled(false);
            btn4.setEnabled(false);
            btn5.setEnabled(false);
            btn6.setEnabled(false);
            btn7.setEnabled(false);
            btn8.setEnabled(false);
            btn9.setEnabled(false);
            btn0.setEnabled(false);
            
            btnaceptar.setEnabled(false);
            btncancelar.setEnabled(false);
            btnborrar.setEnabled(false);
                
            }
            else if(opcretirar==true){
                opcretirar=false;
                
                nuevaCantidad = Double.parseDouble(String.valueOf(txtcantidad.getText()));
                atm.retirar(nuevaCantidad);
                txtsaldo.setText(String.valueOf(atm.obtenerSaldo()));
                
                panelopcion.setVisible(false);
                txtcantidad.setText("");
                btndepositar.setEnabled(true);
                btnretirar.setEnabled(true);
                
            btn1.setEnabled(false);
            btn2.setEnabled(false);
            btn3.setEnabled(false);
            btn4.setEnabled(false);
            btn5.setEnabled(false);
            btn6.setEnabled(false);
            btn7.setEnabled(false);
            btn8.setEnabled(false);
            btn9.setEnabled(false);
            btn0.setEnabled(false);
            
            btnaceptar.setEnabled(false);
            btncancelar.setEnabled(false);
            btnborrar.setEnabled(false);
                
            }
        }
    }//GEN-LAST:event_btnaceptarActionPerformed

